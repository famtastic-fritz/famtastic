#!/usr/bin/env node
/**
 * scripts/plans/drift-reconcile.js — the ZERO-TOKEN drift watchdog.
 *
 * Pure Node, NO LLM calls (respects the all-vendor-brains-cap lesson). It:
 *   1. rebuilds the trace ledger (_system/trace/nodes.jsonl + edges.jsonl),
 *   2. runs audit-all (both universes) via lib/trace-graph + audit.js,
 *   3. writes a durable vault report `_system/trace/reconcile-<date>.md`,
 *   4. prints a one-screen summary to stdout ONLY when drift exists
 *      (watchdog convention: empty stdout = silent = clean run).
 *
 * Designed to be wired to the gateway scheduler later, e.g.:
 *   shay cron create "0 8 * * *" --no-agent --script <wrapper> --deliver telegram
 * (the wrapper just shells `node scripts/plans/drift-reconcile.js`). The logic
 * lives here so there is ONE source of truth; any Python cron wrapper is thin.
 *
 * Exit-code contract: 0 = clean, 2 = drift (same as audit-all.js).
 *
 * Usage:
 *   node scripts/plans/drift-reconcile.js
 *   node scripts/plans/drift-reconcile.js --quiet   # write report, never print
 */

"use strict";

const fs = require("node:fs");
const path = require("node:path");
const tg = require("./lib/trace-graph.js");
const shay = require("./audit-shay.js");
const famAudit = require("./audit.js");

function isoDate() {
  return new Date().toISOString().slice(0, 10);
}

function buildReport(fam, sh) {
  const lines = [];
  lines.push(`# Drift reconcile — ${isoDate()}`);
  lines.push("");
  lines.push("> Generated by `scripts/plans/drift-reconcile.js` (zero-token, no LLM).");
  lines.push("> The reconciler PROPOSES; a human/explicit signal DISPOSES.");
  lines.push("");
  lines.push("## famtastic flat-file universe");
  lines.push(`- active-plan drift: ${fam.drift.length}`);
  lines.push(`- registry/closeout conflicts: ${fam.conflicts.length}`);
  lines.push(`- orphan tasks: ${fam.orphans.length}`);
  for (const d of fam.drift) lines.push(`  - ⚠ plan \`${d.id}\` — zero open tasks`);
  lines.push("");
  lines.push("## Shay universe");
  lines.push("");
  lines.push(`### Detector 1 — owed plan with no fulfilling build task (${sh.owedPlanNoTask.length})`);
  for (const d of sh.owedPlanNoTask) lines.push(`- ⚠ \`${d.plan}\` — ${d.reason} (${d.title})`);
  lines.push("");
  lines.push(`### Detector 2 — ADOPT-NOW capability never tasked (${sh.adoptNowNeverTasked.length})`);
  for (const d of sh.adoptNowNeverTasked) lines.push(`- ⚠ ${d.item} [${d.category}]`);
  lines.push("");
  lines.push(`### Detector 3 — built-but-triage suggestions (${sh.builtButTriage.length})`);
  for (const d of sh.builtButTriage)
    lines.push(`- ◌ ${d.title} [${d.board}/${d.status}] — ${d.evidence}\n  - suggestion: \`${d.suggestion}\``);
  lines.push("");
  return lines.join("\n") + "\n";
}

function buildSummary(fam, sh, famD, shayD) {
  const lines = [];
  lines.push(`🔴 Drift reconcile ${isoDate()} — ${famD + shayD} item(s) (fam=${famD}, shay=${shayD})`);
  if (sh.owedPlanNoTask.length) {
    lines.push(`Owed plans w/ no build task (${sh.owedPlanNoTask.length}):`);
    for (const d of sh.owedPlanNoTask.slice(0, 5)) lines.push(`  • ${d.title}`);
  }
  if (sh.adoptNowNeverTasked.length) {
    lines.push(`ADOPT-NOW never tasked (${sh.adoptNowNeverTasked.length}):`);
    for (const d of sh.adoptNowNeverTasked.slice(0, 8)) lines.push(`  • ${d.item}`);
  }
  if (sh.builtButTriage.length) {
    lines.push(`Built-but-triage — confirm or close (${sh.builtButTriage.length}):`);
    for (const d of sh.builtButTriage.slice(0, 5)) lines.push(`  • ${d.suggestion}`);
  }
  return lines.join("\n") + "\n";
}

function main() {
  const quiet = process.argv.includes("--quiet");

  // 1. rebuild the idempotent ledger
  tg.writeLedger(tg.TRACE_DIR);

  // 2. run both universes
  const fam = famAudit.audit();
  const sh = shay.run();
  const famD = fam.drift.length + fam.conflicts.length + fam.orphans.length;
  const shayD = shay.dirtyCount(sh);
  const dirty = famD + shayD;

  // 3. durable vault report (always written so the dashboard/morning-digest can read it)
  try {
    fs.mkdirSync(tg.TRACE_DIR, { recursive: true });
    fs.writeFileSync(
      path.join(tg.TRACE_DIR, `reconcile-${isoDate()}.md`),
      buildReport(fam, sh)
    );
  } catch {}

  // 4. summary to stdout ONLY if drift (watchdog: empty = clean = silent)
  if (!quiet && dirty > 0) {
    process.stdout.write(buildSummary(fam, sh, famD, shayD));
  }

  return dirty === 0 ? 0 : 2;
}

process.exit(main());
